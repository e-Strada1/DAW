public class Declaraciones_operaciones {
    
    public static void main( String args[]){
        
        int A = 10;
        int B = 5;

        int suma = A+B;
        int resta = A-B;
        int producto = A*B;
        int division = A/B;

        System.out.println("La suma de "+A+" + "+B+" = "+suma);
        System.out.println("La resta de "+A+" - "+B+" = "+resta);
        System.out.println("El producto de "+A+" * "+B+" = "+producto);
        System.out.println("La division de "+A+"/"+B+" = "+division);;
    }
    
}
